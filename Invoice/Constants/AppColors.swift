import UIKit

extension UIColor {
    
    class var appBackgroundColor: UIColor {
        return UIColor(red: 0.949, green: 0.949, blue: 0.949, alpha: 1)
    }
    
    class var appPrimaryColor: UIColor {
        return UIColor(red: 0.165, green: 0.51, blue: 1, alpha: 1)
    }
    
    class var appHintColor: UIColor {
        return UIColor(red: 0.569, green: 0.569, blue: 0.569, alpha: 1)
    }
    
    class var appCaptionColor: UIColor {
        return UIColor(red: 0.64, green: 0.64, blue: 0.64, alpha: 1)
    }
    
    class var appSeparatorColor: UIColor {
        return UIColor(red: 0.871, green: 0.871, blue: 0.871, alpha: 1)
    }
    
}
